    <p class="card-text"> <br>
<br>
Remember the clear blue sky? Mornings heading to work, college and school. <br>
Inhaling the fresh morning air without the need to wear a mask! <br>
Now imagine of a time, where you were suddenly pulled out from wherever you were normally doing and forced to stay back home! <br>
Imagine knowing the fact that your home was going to be your new school/college! <br>
YESS! This is for real. <br>
The petrifying and severe impact of covid 19 has shaken the World to its core. <br>
At first, I thought the "corona virus" wouldn't  last too long
Just like the other products from China. Until I saw its severe impact on people, understood the seriousness of the pandemic, coming to terms with the "lockdown period" and learnt the meaning of the word "QUARANTINE"! <br>
As a student, "every aspect of my daily routine has changed"! <br>
Honestly, we always look forward on getting holidays but, this seems to be the longest separation from a school/college in a student's life. Things have escalated really quick. 
For me, the hardest thing in the "new reality" is the "new reality" itself( doing nothing for almost 2 months). <br>
With the ongoing barriers, the regular life of a student cannot get normalised in the same way as it was until then we have to engage in online classes.
There are postive and negative aspects to everything. <br>
For us this is a completely new change in terms of education is concerned especially in a country like India which is planning to bring about a planned reform in this sector.
Secondly, improvement in learning material, a great start for Universities and colleges to start with advanced learning process. <br>
We as students, can participate in classes from anywhere in the world, provided we have the required gadgets and internet.
As students we will be engaging to a digital world and I feel the best part about E-learning is that the purpose of using phones and computers which was mainly meant for passing time chatting, watching youtube videos, playing games, scrolling through social apps will not be the same anymore. Now, this will become a mean to use internet in a productive way i.e to learn. 
<br>
 I strongly feel that the quality of education and learning that we have had for years will never be replaced by any other mode of teaching
Firstly, a major drawback is adjusting to the "new normal classes" i.e, a sudden shift from lecture based classes to "online classes". This will have direct impact on examinations and students preparations.
Moreover, there is lack of interest and enthusiasm in such classes which definitely results in poor concentration. An online class would never be able to replace traditional education.
The lively environment of your classroom will not be felt through your phones. 
<br>Therefore, learning infront of a Computer or on a phone can never be compared to the charm of going to lectures.
Also, the digital divide describes the gap between those who have access to the internet and those who do not.
It is very difficult for Students who do not have internet or slow connectivity to cope up with this new system. <br>
 <b> "EXAMS"!!! </b><br>
 A word that gave every student goosebumps and a mild terror in their heart is no longer going to make you feel in the same way. The lockdown has generated uncertainty over exam cycle. Due to exams being cancelled "internal assessments" are perhaps thought to be less important,the point of internal assessment was to give information about a students progress for families and teachers. The loss of this information delays recognition of both high pottential and learning difficulties. Thus, having harmful long term consequence for a student. The Worst hit was to the final year students wherein their teaching was interrupted in the final part of their studies, they battled 2 major Things together.
The Nationwide closure is impacting tremendously on our education system.
Going to your college and school is by far the best public policy tool to raise skills, we engage ourselves in social skills and social awareness.
Online learning becomes a new area for both teachers as well as the students, and assesments will likely have larger measurement error than usual.
<br> There is definitely no comparison when it comes to Traditional learning VS E-learning.
You can attend an online class late or not attend it at all with the very convinient excuse saying "NO INTERNET" or "POOR CONNECTIVITY"!
But, it's not the same in a lecture based class.
The main criteria is to teach us "Discipline" which acts as the main pillar for a successful career. It might seem lucrative to few the option of sitting at home and learning at your speed. In a classroom all your distractions are kept aside and the main moto is to pay attention while, it may not be the same when you're home.
In traditional learning you're a part of group discussions, debates, projects which makes you as a student more confident also, gives you an opportunity to interact with your fellow buddies in person and take guidance of your teachers that will only strengthen your understandings on topics better but in the case of Online teaching you'll see a complete absence to such things.
Summing up, I would add by saying that definitely the world of education is threatened. 
It's inevitable that students education has been disrupted by the outbreak of Covid-19.  However, 
there are online classes to ensure that learning is happening any which way.
<br> The government and teachers are doing their best to not put our education on stake which is truly commendable.
This pandemic has thought us many things and gave us lessons to remember. It thought us that as communities and individuals, we change best when we are affected, when our sense of freedom and our quality of life is on stake.
As a student I have realised the vital importance of education how convinient things were and how exhausting it is going to be, to be infront of your screen all day long. it's funny how it takes the entire world coming to stop for us to realise things.
I'm sure we will come out from this pandemic as a more responsible student with better perspective on education and never take it for granted.


